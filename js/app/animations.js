		getPointsOfCircle: function(angle, pointCount, center, radius) {
			var r = radius || animationRadius;
			var c = center || rouletteCenterCoords
			var x, y, circlePoints = [];
			angle = angle * TO_RADIANS;
			var start = (-Math.PI/2);
			var end = -(angle + Math.PI/2);
			var step = (end-start)/pointCount;

			var intervals = [];
			for(var i=start; i>end; i+=step) {
				intervals[intervals.length] = i;
			}

			for(var i in intervals) {
				x = c[0] + r * Math.cos(intervals[i]);
				y = c[1] + r * Math.sin(intervals[i]);
				circlePoints[circlePoints.length] = [x, y];
			}

			return circlePoints;
		},
