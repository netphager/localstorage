	CatmullRomSpline = function(segments) {

		this.getPathLengths = function() {
			dist = [];

			var len = this.segments.length-1;
			for(var i=0; i<len; i++) {
				dist[i] = animations.getDistance(this.segments[i], this.segments[i+1]);
				if(i>0) {
					dist[i] += dist[i-1];
				}
			}

			return dist;
		}

		this.pointAt = function(t) {
			if (!this.segments) return [0,0];
			if (this.segments.length >= 4) {
				var rt = t * (this.segments.length - 3);
				var j = Math.floor(rt);
				var st = rt-j;

				var a = this.segments[j],
				b = this.segments[(j+1)],
				c = this.segments[(j+2)],
				d = this.segments[(j+3)];
				return this.catmullRomPoint(a,b,c,d,st)
			} else {
				return this.segments[0]
			}
		};

		this.pointAngleAt = function(t) {
			if (!this.segments) return {point: [0,0], angle: 0};
			if (this.segments.length >= 4) {
				var segs = this.segments
				// turn t into segment_index.segment_t
				var rt = t * (segs.length - 3)
				var j = Math.floor(rt)
				var st = rt-j
				var a = segs[j],
				b = segs[j+1],
				c = segs[j+2],
				d = segs[j+3];
				return this.catmullRomPointAngle(a,b,c,d,st)
			} else {
				return {point:this.segments[0] || [0,0], angle: 0}
			}
		};

		this.catmullRomPoint = function (a,b,c,d, t) {
			var af = ((-t+2)*t-1)*t*0.5
			var bf = (((3*t-5)*t)*t+2)*0.5
			var cf = ((-3*t+4)*t+1)*t*0.5
			var df = ((t-1)*t*t)*0.5
			return [
				a[0]*af + b[0]*bf + c[0]*cf + d[0]*df,
				a[1]*af + b[1]*bf + c[1]*cf + d[1]*df
			];
		};

		this.catmullRomAngle = function (a,b,c,d, t) {
			var dx = 0.5 * (c[0] - a[0] + 2*t*(2*a[0] - 5*b[0] + 4*c[0] - d[0]) + 3*t*t*(3*b[0] + d[0] - a[0] - 3*c[0]));
			var dy = 0.5 * (c[1] - a[1] + 2*t*(2*a[1] - 5*b[1] + 4*c[1] - d[1]) + 3*t*t*(3*b[1] + d[1] - a[1] - 3*c[1]));
			return Math.atan2(dy, dx)
		};

		this.catmullRomPointAngle = function (a,b,c,d, t) {
			var p = this.catmullRomPoint(a,b,c,d,t);
			var a = this.catmullRomAngle(a,b,c,d,t);
			return {point:p, angle:a};
		};

		this.segments = segments;
		this.distances = this.getPathLengths();

	}

	animationsGLobal = animations;
	return animations;
